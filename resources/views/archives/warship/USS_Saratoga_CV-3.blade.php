                    <h3>原型简介</h3>
                    <hr>
                    <h4><b>舰船设计</b></h4>
                    <p>
                        萨拉托加号航空母舰是美国海军列克星敦级航空母舰的2号舰（但无论动工、下水、服役均略早于1号舰），名字来源于美国独立战争的转折战役：1777年萨拉托加战役。1920年9月25日在纽约造船公司动工，1922年7月1日被下令改建成航空母舰，1925年4月7日下水，1927年11月16日正式服役。列克星敦级航空母舰的船体原本是作为美国首次尝试建造的战列巡洋舰设计的，但由于《华盛顿海军条约》的限制，美国不得不放弃建造6艘战列巡洋舰的计划，并将建造进度最快的“列克星敦号”和“萨拉托加号”改建为航空母舰。
                    </p>
                    <p>
                        萨拉托加号航空母舰，标准排水量36000长吨（36578吨），满载排水量43055长吨（43746吨）【49552长吨（50347吨）——1945年改装后】，采用封闭舰首和单层机库的设计，全通式甲板长达271米，宽度31米，吃水9米。装配两部升降机，最多可搭载超过100架舰载机（设计容量78架飞机，包括甲板系留和机库，后利用在机库顶部悬挂备用飞机的方法将载机数量扩大到109架），最大航速33.25节。由于最初作为战列巡洋舰设计，船体防护能力和抗沉性可以媲美巡洋舰,主装甲带178mm，定员接近13000人。建造时还装备了4座双联装8英寸火炮。
                    </p>
                    <h4><b>关于8英寸炮</b></h4>
                    <p>
                        列克星敦级航空母舰由原列克星敦级战列巡洋舰改造而来，起初美国海军的想法是将该舰建设成”有多功能用途的巡洋舰“，故装备了4座双联装8英寸火炮。起初，海军上将米切尔对这8门8英寸炮十分自豪，但该炮不仅为了飞行甲板牺牲了左侧射界，使全舰重心偏右，其炮口风暴还会对飞行甲板和甲板上的飞机造成损害。在“舰队问题演习9”中，萨拉托加使用8英寸炮发射空包弹时，其炮口风暴震坏了距离舰炮21米的1架T3M鱼雷机，该机36条肋骨损坏，并被撕下大块的蒙皮。而且纵观整个航母的战争历史，除了光荣号迷糊少女以外，航空母舰基本没有和敌舰进行（被）炮击的战例。故在1942年将该炮完全拆除，并增加右侧突出部平衡全舰，使该舰获得了更好的续航力和防空火力。萨拉：不嘛人家就要用8英寸炮和别人对轰嘛！
                    </p>
