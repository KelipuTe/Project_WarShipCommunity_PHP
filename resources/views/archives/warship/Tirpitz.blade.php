                    <div style="float:left;width:730px;">
                        <p><b>提尔比茨号战列舰</b>（或译作“提尔皮茨”、“铁必制”），是俾斯麦级战列舰的2号舰，即俾斯麦号战列舰的姊妹舰，1936年11月2日于威廉港战争海军造船厂动工，1939年4月1日下水，1941年2月25日服役。
                        </p>
                        <p>
                            提尔比茨标准排水量42300长吨（42900吨），设计最高航速30节左右，装备了4座双联装380毫米主炮。与姐妹舰俾斯麦一样采用全面防护，拥有同期战列舰中的最大防护范围，装甲占标准排水量的40%以上。</p>
                        <h4><b>战争家里蹲经历</b></h4>
                        <p>
                            由于俾斯麦的沉没以及其他水面舰只攻击商船上的失利，希特勒丧失了对水面舰队的信任，该舰长期处于一种“限制行动”的状态下。1942年3月英国皇家海军陆战队突袭了法国圣纳泽尔的大型船坞，令德国唯一能够在大西洋维修提尔比茨号战列舰的大型船坞被破坏，提尔比茨被迫前往挪威北部水域并在这里度过了二战中的大部分时光。</p>
                        <p>
                            提尔比茨之后一直是挪威北部海域德国舰队的主力，但基本上是在挪威各个港口之间来回，作为威慑以牵制皇家海军的力量。提尔比茨号在挪威时进行了3次出击，但仅在1943年获得过对敌开火的机会。由于提尔比茨的这些行动，挪威人给了她一个“北方孤独宅女女王”的称号。
                            成天呆在挪威无所事事，要么就抱头蹲防等RAF丢炸弹。</p>

