                    <h3>原型简介</h3>
                    <hr>
                    <p><b>俾斯麦号战列舰</b>（德语：Bismarck）是德国海军俾斯麦级战列舰的首舰，于1936年7月1日在汉堡布洛姆·沃斯船厂动工，1939年2月14日下水，1940年8月24日服役。</p>
                    <p>
                        俾斯麦号战列舰标准排水量达41000长吨（41700吨），是当时德国最大的战舰，也是纳粹德国上台后研制的第二型战列舰。1935年德国宣布废除禁止其建造军舰的《凡尔赛条约》，并向英国提出限制德国海军总吨位为英国海军的35%，英国欣然接受并与之签订《英德海军条约》，为德国建造大型战舰铺平了道路。</p>
                    <p>
                        俾斯麦号是德国一战战败后时隔二十年首次研究的新型战列舰，为了减少风险和加快研发速度，尽管英德海军条约规定德国战舰火炮口径上限为16英寸，德国人还是决定在一战时研制的380毫米火炮基础上研制新的380毫米火炮。事实上SK/C34初速不仅被砍了，也是欧洲倒第二15寸(倒第一是4crh弹之英畜15寸MK1)，欧洲新式战列舰主炮倒数第一。</p>
                    <p>俾斯麦号最终装备了前后各两座双联装380毫米火炮，沿用了全面防护方式布置的装甲覆盖了70%的水线长度，装甲重量超过标准排水量的40%。</p>
                    <p>
                        全面防护不仅占用吨位大，并且它特有的穹甲——也是世界已成舰中第二厚的穹甲——压缩空间拉长了核心舱导致装甲带被迫拉长，重量也是蹭蹭地往上涨，因此不少人戏称这是“吨位换智商”或“德三在用吨位弥补20年来缺失的智商”。再者，看装甲带长度正常情况下并无什么意义，德三的主力舰还是为了舰队决战而存在的，其50mm露天甲板+80mm重叠于弹道上的水平装甲——德国人将水平装甲从其他国家流行的与主装上部相连变为以穹甲连接下部，主水平装甲一定程度上位于近距离的弹道上，110mm的穹甲倾斜部配合320mm的垂直主装将发挥作用——太薄以至于远距离防护感人。此外，俾斯麦级装甲风格实际延续一战巴伐利亚级，而一战恰恰不注重水平防护。同时，俾斯麦级装甲唯一优势在万码以下的一战设计思路，导致垂直方向防护不错，水平方向简直是惨不忍睹。据英国推算，俾斯麦级在与乔治五世级在22000码距离上交战时，只有20%部分位于主装保护下，并且英舰炮弹有相当大概率穿透甲板装甲和主水平装甲或上部装甲带还主水平装甲而直接砸进发射药库——德国船与英国不同，发射药库直接位于主水平装甲下方，无防破片装甲——导致殉爆。或者水中弹导致大量进水。</p>

