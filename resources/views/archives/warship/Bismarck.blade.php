                        <h4><b>战争经历</b></h4>
                        <p>
                            1941年5月19日早上，俾斯麦与欧根亲王号重巡洋舰秘密离开波兰格丁尼亚的港口，展开“莱茵演习行动”，准备进入大西洋拦截来往英国和北美洲的运输船队，切断英国的海上补给线，但随后两舰行踪在挪威卑尔根附近海岸先后被瑞典巡洋舰和英国侦察机发现，英国皇家海军调动舰队前往拦截。5月24日清晨，双方在丹麦海峡爆发海战，俾斯麦在当天早晨6点钟整击中英方战舰胡德号战列巡洋舰的弹药库使其殉爆，胡德号当场被击沉，随后在威尔士亲王号战列舰以一敌二并且炮塔故障火力无法全部发挥的情况下，和俾斯麦进行了短暂炮战后离开战场。丹麦海峡海战结束后，俾斯麦由于在战斗中被威尔士亲王号战列舰在早晨5时56分到59分的第6和第9轮齐射的命中击穿了舰体，导致燃油泄漏并导致2号锅炉停工，迫使放弃驶入大西洋计划，与欧根亲王号分开，准备返回法国圣纳赛，但在途中被追击的英国舰队发现。5月26日，由皇家方舟号航空母舰起飞的剑鱼式鱼雷轰炸机所发射的鱼雷击中了俾斯麦号，导致俾斯麦号的尾舵被卡死在左12度，同时轮机舱破损进水，不得不以7节的航速向英国舰队方向驶去。5月27日早上，比斯开湾俾斯麦号终于被英国舰队追上，开战后数分钟后就被罗德尼号战列舰击毁了前面两座炮塔，9点31分罗德尼号瘫痪了俾斯麦的所有主炮，在承受了英舰罗德尼号和乔治五世国王号两小时对上层建筑报复性的狂轰滥炸后，在鱼雷和自行打开通海阀的共同作用下在19分钟内快速沉没德意志通海阀科技世界第一。</p>
                        <p>事实上俾斯麦号防空系统设计并非不堪一击 ，只是当时该舰同时装备了双联10.5 cm/65 SK C/33和双联10.5 cm/65 SK
                            C/37高炮，两者炮架不同，前者原为舰载88高炮的炮架你说我一个88炮的炮架，怎么让我扛105去了呢，后者是专门为105高射炮设计的，两者俯仰速度相差不小，所以火控不佳提前量过大（德系引以为傲的火控都没了），导致了远距离上“剑鱼飞的太慢打不下来”，在中距离又不能被37mm双联防空炮有效击落的情况，在俾斯麦被击沉后，德国海军将提尔比茨号上的105高射炮全部换成了统一型号37虽烂，请不要忘记是火控的锅哟~当时德国陆军的37高也比这个强，造出这门炮的和决定把这门炮装上船的都该拉去当装填手。（这是可能性之一，没有确凿证据显示是因为这个原因。）（导致俾斯麦号打不中剑鱼的可能性有多种当然负责中近距离防御的37手拉机是肯定得背锅的。其一是安装在俾斯麦号上的10.5cm
                            SK
                            C/33高炮采用了2种炮座混装的方式，两种炮座的回转和俯仰速率不同，而火控系统显然忘了考虑这一点，导致了高炮无法有效地进行防御；其二是俾斯麦号采用的高炮火控系统在应对低速目标时效果较差（这一可能性得到了诺曼·弗里德曼先生的肯定）；其三是引信测合机未校准导致炮弹早炸。没有确凿的结论。攻击俾斯麦号的剑鱼上其实都有百多个弹孔，其中一架甚至有175个，这些弹孔的主要贡献者是20mm机炮（炮弹没有被机身帆布引爆），不过20mm机炮的有效射程还不如鱼雷机的投雷距离，只在剑鱼飞过头顶时才有效果。而37手拉机射速不足，造成了德舰在中近距离存在火力真空。</p>
                        <p>
                            其实这完全是因为在二战以前各国完全没有太多舰队防空袭经验。而俯冲轰炸和鱼雷机也是在二战时期成熟。二战之前空军的轰炸精度和炸弹的威力由于飞机本身的限制完全不够看，海军航空兵的小飞机更是如此，因此二战时期（对于部分国家甚至直到二战中期）很多国家的海军依旧大幅低估了飞机的威力而没有对防空武器有太多关注，依旧抱持着大舰巨炮是决战武器的思想，直到被飞机鱼雷大炸弹玩了个爽。就连美国，换装真正有用的防空炮(瑞典产博福斯40mm高炮+127mm近炸引信炮弹)也是中途岛以后的事情。所以德国战列舰没有有效的防空能力也是情有可原。</p>
                        <p>
                            而且即使改装后装备大量高炮，不论是威尔士亲王还是日本大多数战舰最后的命运也无不说明以战列舰巨大的体积和较慢的航速想在没有空中支援的空袭中存活是一种拼信仰的想法。然而日本船防空差，威尔士亲王战术有误、武器故障、人品雷断电想不死都难</p>
