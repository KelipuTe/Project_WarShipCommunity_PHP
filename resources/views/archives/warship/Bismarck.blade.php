                        <h4><b>沉没原因19分钟通海阀</b></h4>
                        <p>不少德系粉丝对俾斯麦的强大产生了过分夸张的印象。他们坚称俾斯麦不是被击沉的，而是“打开通海阀”自沉的。</p>
                        <p>
                            他们的依据是俾斯麦幸存官兵的回忆。据老兵回忆，当时他们在突围无望，全舰丧失战斗力时打开了通海阀试图自沉。此外，卡梅隆拍摄的纪录片《沉没的俾斯麦战舰》中对俾斯麦残骸进行的“水下考古”中，探查到的部分没有受到严重损伤。因此他们得出结论，俾斯麦没有在战斗中受到重伤，之所以沉没是因为船员自沉。</p>
                        <p>
                            然而，纪录片中的“水下考古”并没有探查到涉及战舰核心区的受损情况，所有的结论都是间接合成得来的，并没有学术价值。此外，俾斯麦官方纪念网站用数据打了所有支持俾斯麦自沉论的人的脸：资料显示俾斯麦于10点20分打开通海阀，于10点39分沉没，其间仅用时19分钟。</p>
                        <p>
                            作为比较，1919年的公海舰队自沉事件中，排水量只有俾斯麦一半的腓特烈大帝在打开全部水密门的非战斗条件下，打开通海阀后用时一个多小时才沉没。而26500吨的兴登堡号则坚持了6个小时。1942年，36600吨的雾岛，在身受重伤的情况下，打开通海阀后用时近2个小时才沉没。除非之前就已经大量进水，否则排水量41700吨的俾斯麦不可能在战斗条件下用19分钟就快速沉没。换言之，俾斯麦在德国人采取自沉措施前，就已经开始了不可逆的下沉。</p>
                        <p>于是19分钟通海阀就成为了嘲讽无视事实，过分吹嘘的某些德系粉丝的著名梗。讽刺那些人罔顾事实，一味沉浸于自己的幻想中。</p>
                        <p>
                            当然，在俾斯麦究竟是否是自沉的问题上依然有其它说法。例如“炸药自沉说”，依据是一部分水兵事后的回忆：收到自沉要求所有水兵上甲板等待落水待援的命令时候听到了底下一声解体声响。如果不是英国人的鱼雷，那么就是德国人自己的炸药。德国船员采取了自沉行动，这一点是毫无争议的。它和英国人的打击一起，促使了俾斯麦的最终沉没。</p>
                        <p>
                            其实讨论船员的自毁和英国人的打击对俾斯麦沉没起到的作用究竟是谁更大根本毫无意义。因为当时的俾斯麦已经丧失了战斗能力，足以说明她并非坚不可摧。而且此时的她已经沦为英国皇家海军的靶船，可以说是一艘“浮着的沉船”了。讨论究竟是否是德国人自沉最终导致俾斯麦沉没，都无妨于俾斯麦被英军以优势兵力歼灭的事实。</p>
                        <p>雾岛：谁说我是被击沉的？咱是自沉的好么！咱打开通海阀了！凭什么给华盛顿计人头！</p>
