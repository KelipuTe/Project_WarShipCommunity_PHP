                    <h3>原型简介</h3>
                    <hr>
                    <h4><b>建造</b></h4>
                    <p><b>欧根亲王号</b>是一艘隶属于德国海军的重巡洋舰，为希佩尔海军上将级重巡洋舰的3号舰。名字来源于奥地利历史上一名陆军元帅欧根亲王。用奥地利人为该舰命名主要是为了强调德奥两国的历史渊源，粉饰当时德国对奥地利的吞并，手动眼斜、但是把俾斯麦坑掉算报此大仇。
                    </p>
                    <p>法国的伏尔泰评价欧根亲王本人为“那个时代最完美的军人，拥有一位伟大将领所应有的一切优良素质，是战时的英雄、和平中的伟人。”</p>
                    <h4><b>战争经历</b></h4>
                    <p>
                        欧根亲王号建造于20世纪30年代中叶。1936年4月23日，欧根亲王号的在基尔克虏伯船厂开始铺设龙骨，1938年8月22日，欧根亲王号下水并于1940年8月1日服役。1941年4月参加“莱茵演习”行动，5月24日与“俾斯麦”号（Bismarck）战列舰一起参加了著名的丹麦海峡海战，先后击中英国皇家海军“胡德”号（HMS
                        Hood）战列巡洋舰和“威尔士亲王”号（HMS Prince of Wales）战列舰。其后因为俾斯麦的受损而必须跟俾斯麦分开行动，躲过了被英国皇家海军围炉的命运。</p>

                    <div style="float:left;width:730px;">
                        <p>
                            作为被德国海军寄予厚望的重巡洋舰，欧根亲王号终其一生未能独立击沉任何敌方军舰，但却奇迹般地从每一次恶战当中得以生还，堪称是德国海军的传奇。然后每次脱离护航队伍之后不久被她护航的主力舰都非沉即大破，俾斯麦幸运A都躺枪元首の雪风后来在42年初撤回德国本土的地狱犬-雷霆行动中，欧根亲王毫发未伤，而德国版不幸姊妹沙恩霍斯特与格奈森瑙都有触雷受创。祥瑞身份立场再次确认。剩下的数年间，在支援东线岸轰以及战争末期的东普鲁士大撤退，之后迎来终战，是德国终战时唯一的大型水面战斗舰艇。它亲历了整个第三帝国由盛转衰的历史，战后被赔偿给美国。悲惨经历的高潮。</p>
                        <p>
                            1946年7月，参加了美国在比基尼环礁举行的核试验，与内华达、萨拉托加等船一起充当原子弹的靶舰。在历经两次核爆观赏人工太阳以后，虽仍然未沉没，但受到严重的放射性污染已成废舰，被拖往夸贾林环礁弃置祥瑞御免，家宅平安。由于船体带有放射性无法修理，12月22日在夸贾林环礁进水沉没。</p>

