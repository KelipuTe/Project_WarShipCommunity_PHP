                    <h3>原型简介</h3>
                    <hr>
                    <h4><b>建造</b></h4>
                    <p>列克星敦号航空母舰(USS Lexington CV-2)是美国海军列克星敦级航空母舰的首舰，名字来源于美国独立战争中的第一枪：1775年列克星敦之战。</p>
                    <p>
                        列克星敦号于1921年1月8日在富尔里弗造船公司动工，1922年7月1日被下令改建成航空母舰，1925年10月3日下水，1927年12月14日正式服役。列克星敦级航空母舰的船体原本是作为美国首次尝试建造的战列巡洋舰设计的，但由于《华盛顿海军条约》的限制，美国不得不放弃建造6艘战列巡洋舰的计划，并将建造进度最快的“列克星敦号”和“萨拉托加号”改建为航空母舰。
                    </p>
                    <p>
                        列克星敦号航空母舰，标准排水量36000长吨（36578吨），采用封闭舰首和单层机库的设计，全通式甲板长达271米，最多可搭载超过100架舰载机（设计容量78架飞机，包括甲板系留和机库，后利用在机库顶部悬挂备用飞机的方法将载机数量扩大到109架），设计最大航速33.25节（1928年海试最高航速为34.59节）。由于最初作为战列巡洋舰设计，船体防护能力和抗沉性可以媲美巡洋舰。建造时装备了4座双联装8英寸火炮，直到1942年4月拆除。
                    </p>
                    <h4><b>关于8英寸炮</b></h4>
                    <p>
                        列克星敦级航空母舰由原列克星敦级战列巡洋舰改造而来，起初美国海军的想法是将该舰建设成”有多功能用途的巡洋舰“，故在其右舷装备了4座双联装8英寸火炮，在舰桥前、烟囱后二二布置。起初，海军上将米切尔对这8门8英寸炮十分自豪，但该炮不仅为了飞行甲板牺牲了左侧射界，其炮口风暴还会对飞行甲板和甲板上的飞机造成损害。4座炮塔全体右置也挤占了重量和一部分甲板空间，使列克星敦级的舰体长期处于左轻右重的困扰中。（该设计虽然并不合理，但在当时在航母上加装火炮是普遍的设计，因为在海军航空兵刚刚兴起时没有人知道航母要如何运用）。不过战争爆发后，面对新的海战模式，这四座火炮终于在1942年4月在珍珠港海军基地拆除。但是列克星敦号在一个多月后的珊瑚海海战中早早战沉也使这几门炮成为太太少有的遗物。
                    </p>

