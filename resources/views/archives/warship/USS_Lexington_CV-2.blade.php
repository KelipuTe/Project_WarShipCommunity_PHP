                                <h4><b>战前岁月</b></h4>
                                <p>
                                    服役后，列克星敦号和萨拉托加号参加了美国海军的大量演习，为美国海军探索航空母舰的运用积累了大量的经验，促进了航母核心战术的发展。其中最著名的是第九次舰队演习即“舰问⑨”，只不过这次演习的主角其实应该是妹妹萨拉托加。
                                </p>
                                <p>1934-1936年间进行几次小规模的改造，在1936年的改造中其舰首甲板被拓宽。</p>
                                <h4><b>战争经历</b></h4>
                                <p>
                                    1941年，战争的乌云开始笼罩在太平洋上空，列克星敦号航空母舰被编入太平洋舰队，主要驻扎在珍珠港。12月5日，列克星敦号按计划离港向中途岛基地运送飞机，从而与12月7日日本偷袭珍珠港的行动擦肩而过。12月13日，列克星敦号返回珍珠港，14日受命突袭贾鲁伊特环礁，20日因威克岛局势危机转而驰援威克岛，但在抵达之前威克岛已经落陷。
                                </p>
                                <p>
                                    1942年，在袭击了新几内亚岛的日军后，列克星敦号作为旗舰带领第11特混舰队出击，并于5月1日与带领第17特混舰队的约克城号航空母舰在珊瑚海会合。5月7日，列克星敦号和约克城号的舰载机击沉了日本联合舰队的祥凤号轻型航空母舰，次日两舰的舰载机又击伤了翔鹤号航空母舰，并重创了瑞鹤号航空母舰的舰载机部队。两次战斗直接导致日本联合舰队第五航空战队无法参加一个月后的中途岛战役。太太们之间的撕X也是可怕。在战斗中列克星敦号至少被命中了2条航空鱼雷和3枚航空炸弹，虽然火势一度得到控制，并且可以以25节航速自行返航。但不幸的是，5月8日中午由于泄漏的航空燃油蒸发后聚集在封闭舰首的机库中被引燃而发生了爆炸，17时舰长下达了弃舰命令，随后驱逐舰菲尔普斯（USS
                                    Phelps DD-360）进行了雷击处分。20时，列克星敦号航空母舰沉没。
                                </p>
                                <p>她在二战中，一共获得了两枚战斗之星。</p>
                                <p>1942年6月24日，美军公开承认列克星敦号沉没，并正式除籍。大致沉没在155°E,15°S，喜欢太太的人有生之年能去看一眼她也会很开心吧。</p>
